
// EXERC06 - PROGRAMA QUE APRESENTA UMA CONTAGEM REGRESSIVA COMEÇANDO
// DE 10 ATÉ 0 MOSTRANDO UMA MENSAGEM "FIM" AO TÉRMINO

public class App {
    public static void main(String[] args) throws Exception {
        
        int i;

        for ( i = 10; i >= 0; i--){
            System.out.println(i);
        }
        System.out.println("FIM!");
    }
}
